    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:

        int selectedRowIndex = tblMedicine.getSelectedRow();

        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to delete.");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tblMedicine.getModel();
        Medicine selectedMedicine = null;
        
        String medicineName = model.getValueAt(selectedRowIndex, 1).toString();

        for (Medicine m : ecoSystem.getMedicineDirectory().getMedicineList()) {
            if(m.getMedicineName().equals(medicineName)){
                selectedMedicine = m;
            }
        }
        ecoSystem.getMedicineDirectory().deleteMedicine(selectedMedicine);
        Store.save(ecoSystem);

        JOptionPane.showMessageDialog(this, "Medicine deleted.");

        populateMedTable();
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        // TODO add your handling code here:

        int productId = Integer.parseInt(txtproductId.getText());
        String medicineName = txtmedicineName.getText();
        String expiryDate = txtexpiryDate.getText();
        int totalQuantity = Integer.parseInt(txtavailQuantity.getText());
        double totalPrice = Double.parseDouble(txtPrice.getText());

        //Equipment equipment = new Equipment(equipmentId, equipmentName, equipmentReqID, equipmentPrice, totalQuantity);
        ecoSystem.getMedicineDirectory().addMedicine(productId, medicineName, expiryDate, totalQuantity, totalPrice);
        Store.save(ecoSystem);
        JOptionPane.showMessageDialog(this, "Medicine added successfully.");

        txtproductId.setText("");
        txtmedicineName.setText("");
        txtexpiryDate.setText("");
        txtavailQuantity.setText("");
        txtPrice.setText("");

        populateMedTable();
    }//GEN-LAST:event_btnaddActionPerformed

