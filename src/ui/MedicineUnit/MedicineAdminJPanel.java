    private void populateMedTable() {
        ArrayList<Medicine> medicineList = ecoSystem.getMedicineDirectory().getMedicineList();

        DefaultTableModel model = (DefaultTableModel) tblMedicine.getModel();
        model.setRowCount(0);

        for (Medicine m : medicineList) {
            Object[] row = new Object[5];
            row[0] = m.getProductId();
            row[1] = m.getMedicineName();
            row[2] = m.getExpiryDate();
            row[3] = m.getAvailQuantity();
            row[4] = m.getSellingPrice();
            model.addRow(row);
        }
    }
