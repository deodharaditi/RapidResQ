    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:     

        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please Select a row from table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String billingRequestId = jTable1.getModel().getValueAt(selectedRow, 0).toString();
        for (WorkRequest wr : billingOrg.getWorkQueue().getWorkRequestList()) {
            if (wr instanceof BillingWorkRequest) {
                BillingWorkRequest billWrkReq = (BillingWorkRequest) wr;
                if (billWrkReq.getBillingRequestId().equals(billingRequestId)) {
                    this.billingWorkRequest = billWrkReq;
                }
                billWrkReq.setStatus("Processing");
                JOptionPane.showMessageDialog(this, "Bill generated!");
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

