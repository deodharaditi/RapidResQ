    public void populateRequestTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        model.setRowCount(0);

        for (Organization organization : ecoSystem.getOrganizationDirectory().getOrganizationList()) {
            if (organization instanceof BillingOrg) {
                this.billingOrg = (BillingOrg) organization;
                break;
            }
        }

        for (WorkRequest wr : billingOrg.getWorkQueue().getWorkRequestList()) {
            if (wr instanceof BillingWorkRequest) {
                BillingWorkRequest billWrkReq = (BillingWorkRequest) wr;
                Object[] row = new Object[7];

                row[0] = billWrkReq.getEquipment().getEquipmentID();
                row[1] = billWrkReq.getSender().getUsername();
                row[2] = billWrkReq.getEquipment().getEquipmetName();
                row[3] = billWrkReq;
                row[4] = billWrkReq.getEquipment().getTotalCost();
                row[5] = billWrkReq.getEquipment().getAvailableQuant();
                row[6] = billWrkReq.getStatus();

                model.addRow(row);
            }

        }
    }

