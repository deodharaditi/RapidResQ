    private void btnAssignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignActionPerformed
        // TODO add your handling code here:

        int selectedRow = tblPatients.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please Select a row from table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String requestId = tblPatients.getModel().getValueAt(selectedRow, 0).toString();
        for (WorkRequest wr : emergencyOrg.getWorkQueue().getWorkRequestList()) {
            if (wr instanceof EmergencyWorkRequest) {
                EmergencyWorkRequest emergencyWorkRequest = (EmergencyWorkRequest) wr;
                if (emergencyWorkRequest.getEmergencyRequestId().equals(requestId)) {
                    this.emergencyWorkRequest = emergencyWorkRequest;
                }
                emergencyWorkRequest.setStatus("Processing");
                JOptionPane.showMessageDialog(this, "Pickup assigned!");
            }
        }
    }//GEN-LAST:event_btnAssignActionPerformed

