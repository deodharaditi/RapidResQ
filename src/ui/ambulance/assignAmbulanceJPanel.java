    public void populateRequestTable() {
        DefaultTableModel model = (DefaultTableModel) tblPatients.getModel();

        model.setRowCount(0);

        for (Organization organization : ecoSystem.getOrganizationDirectory().getOrganizationList()) {
            if (organization instanceof EmergencyOrg) {
                this.emergencyOrg = (EmergencyOrg) organization;
                break;
            }
        }

        for (WorkRequest wr : emergencyOrg.getWorkQueue().getWorkRequestList()) {
            if (wr instanceof EmergencyWorkRequest) {
                EmergencyWorkRequest emerWrkReq = (EmergencyWorkRequest) wr;
                Object[] row = new Object[4];

                row[0] = emerWrkReq.getEmergencyRequestId();
                row[1] = emerWrkReq.getPatient().getPatientId();
                row[2] = emerWrkReq.getPatient().getPatientName();
                row[3] = emerWrkReq.getStatus();

                model.addRow(row);
            }

        }
    }

