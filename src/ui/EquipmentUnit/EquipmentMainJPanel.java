    private void populateEquipTable() {
        ArrayList<Equipment> equipmentList = ecosystem.getEquipmentList().getEquipmentList();

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        for (Equipment e : equipmentList) {
            Object[] row = new Object[4];
            row[0] = e.getEquipmentID();
            row[1] = e.getEquipmetName();
            row[2] = e.getTotalCost();
            row[3] = e.getAvailableQuant();
            model.addRow(row);
        }
    }
