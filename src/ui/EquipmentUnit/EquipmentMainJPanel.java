    private void btnRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRequestActionPerformed
        // TODO add your handling code here:

        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please Select a row from table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String equipmentId = jTable1.getModel().getValueAt(selectedRow, 0).toString();
        for (Equipment e : this.equipmentList.getEquipmentList()) {
            if (e.getEquipmentID().equals(equipmentId)) {
                this.equipment = e;
            }
        }

        BillingWorkRequest request = new BillingWorkRequest();
        request.setSender(account);
        request.setStatus("Sent");
        request.setEquipment(this.equipment);

        Organization org = null;
        for (Organization organization : ecosystem.getOrganizationDirectory().getOrganizationList()) {
            if (organization instanceof BillingOrg) {
                org = organization;
                ecosystem.getOrganizationDirectory().getOrganizationList().remove(org);
                org.getWorkQueue().addWorkRequest(request);
                //account.getWorkQueue().addWorkRequest(request);
                ecosystem.getOrganizationDirectory().getOrganizationList().add(org);
                Store.save(ecosystem);
                break;
            }
        }

        JOptionPane.showMessageDialog(null, "Request sent!");
    }//GEN-LAST:event_btnRequestActionPerformed

